package dao.webtoon;

public class WebtoonDTO {
	String webtoon_idx = "";
	String review_idx = "";
	String webtoon_title = "";
	String webtoon_content = "";
	public String getWebtoon_idx() {
		return webtoon_idx;
	}
	public void setWebtoon_idx(String webtoon_idx) {
		this.webtoon_idx = webtoon_idx;
	}
	public String getReview_idx() {
		return review_idx;
	}
	public void setReview_idx(String review_idx) {
		this.review_idx = review_idx;
	}
	public String getWebtoon_title() {
		return webtoon_title;
	}
	public void setWebtoon_title(String webtoon_title) {
		this.webtoon_title = webtoon_title;
	}
	public String getWebtoon_content() {
		return webtoon_content;
	}
	public void setWebtoon_content(String webtoon_content) {
		this.webtoon_content = webtoon_content;
	}
	
	
}
